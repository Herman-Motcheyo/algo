package utils;

public class Objets {
	private double poids;
	private double valeur;
	public Objets(double poids, double valeur) {
		super();
		this.poids = poids;
		this.valeur = valeur;
	}
	public double getPoids() {
		return poids;
	}
	public void setPoids(double poids) {
		this.poids = poids;
	}
	public double getValeur() {
		return valeur;
	}
	public void setValeur(double valeur) {
		this.valeur = valeur;
	}


}
